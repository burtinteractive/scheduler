<h4>About</h4>
<p>
    This is a meeting scheduler that can work with your own DB and CAS to generate meeting request similar to that or doodle.

</p>
<h4>Sending Emails</h4>
<p>
    <b>To Email Field<br/></b>
    Emails will auto fill automatically. A small box will pop up where you can view the email you want to select. 
    Once picked, it will populate in the To input field followed by a semi-colon. Now you can enter another email.
</p>
<p>
    <b>CC Email Field<br/></b>
    Emails will autofill. A small box will pop up where you can view the email you want to select. 
    Once picked, it will populate in the CC input field followed by a semi-colon. Now you can enter another email.
</p>
<p>
    <b>Email List<br/></b>
    One can select from the list of emails by department and add the whole department then submit and email to all. 
</p>
<p>
    <b>Emails to people not in your organization<br/></b>
    You can send emails to people not currently in your organization. The autofill function will not work since
    they are not in the system. It will still send them a meeting email and they can respond as well. They will not be able to create a meeting.
</p>
<h4>Update and Request Changes</h4>
<p>
    <b>How do I update my original request</b>
    You can login to the scheduler and go to the My Meetings tab and all your meetings will be listed.
    Or, you can view the initial email that you get upon meeting creation and view that particular meeting with that link.
    Once inside a meeting, all the information will be populated from what you filled out before. Just change those fields and hit send 

    again and it will send the updated changes to your attendees and alert them that this meeting has been changed.
</p>

<h4>Time Entries</h4>
<p>
    <b>Military Time<br/></b>
    You can enter military time into the time slots and it will reformat it into standard time format.
    <b>Time Range</b>
    You can enter in a time range. Time1 followed by the dash "-" the time2 would be the most appropriate format.
</p>


<h4>My Meetings</h4>
<p>
    <b>About<br/></b>
    The My Meetings tab will display every meeting that you have ever entered in using the system. If you click on it 
    it will take you to the meeting and you can view who has responded and who has not.
</p>

